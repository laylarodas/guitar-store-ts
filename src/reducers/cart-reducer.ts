import { Guitar, CartItem } from "../types";
import { db } from "../data/db";


export type CartActions = 
{ type: 'add-to-cart', payload: { item: Guitar} } |
{ type: 'remove-from-cart', payload: { id: Guitar['id']} } |
{ type: 'decrease-quantity', payload: { id: Guitar['id']} } |
{ type: 'increase-quantity', payload: { id: Guitar['id']} } |
{ type: 'clear-cart' } 


export type CartState = {
    data: Guitar[]
    cart: CartItem[]
}

export const initialState : CartState = {
    data: db,
    cart: []
}

const MIN_ITEMS = 1
const MAX_ITEMS = 5

export const cartReducer = (
    state: CartState = initialState, 
    action: CartActions
    ) => {

    if(action.type === 'add-to-cart') {


        const itemExists = state.cart.find(guitar => guitar.id === action.payload.item.id)

        let updatedCart : CartItem[] = []

        if(itemExists) { // existe en el carrito
            updatedCart = state.cart.map( item => {
                if(item.id === action.payload.item.id && item.quantity < MAX_ITEMS) {
                    return {
                        ...item,
                        quantity: item.quantity + 1
                    }
                }
                return item
            })

        } else {
            const newItem : CartItem = {...action.payload.item, quantity : 1}
            updatedCart = [...state.cart, newItem]

        }

        return {
            ...state,
            cart: updatedCart
        }
    }


    if(action.type === 'remove-from-cart') {
        return {
            ...state,
            cart: state.cart.filter(guitar => guitar.id !== action.payload.id)
        }
    }

    if(action.type === 'decrease-quantity') {
        const updatedCart = state.cart.map( item => {
            if(item.id === action.payload.id && item.quantity > 1) {
                return {
                    ...item,
                    quantity: item.quantity - 1
                }
            }
            return item
        })
        return {
            ...state,
            cart: updatedCart
        }
    }

    if(action.type === 'increase-quantity') {
        const updatedCart = state.cart.map( item => {
            if(item.id === action.payload.id && item.quantity < 5) {
                return {
                    ...item,
                    quantity: item.quantity + 1
                }
            }
            return item
        })
        return {
            ...state,
            cart: updatedCart
        }
    }

    if(action.type === 'clear-cart') {
        return {
            ...state,
            cart: []
        }
    }

    return state
}